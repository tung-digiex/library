    void load() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblNhatKi.getModel();
        model.setRowCount(0);
        try {
            List<NhatKiNhapSach> list = dao.select();
            for (NhatKiNhapSach nk : list) {
                Object[] row = {
                    nk.getStt(),
                    nk.getMaSach(),
                    nk.getSoLuong(),
                    DateHelper.toString(nk.getNgayNhap())
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }    
        
        public NhatKiNhapSach getModel() {
        NhatKiNhapSach model = new NhatKiNhapSach();
        model.setMaSach(txtMaSach.getText());
        model.setSoLuong(Integer.valueOf(txtSoLuong.getText()));
        model.setNgayNhap(DateHelper.toDate(txtNgayNhap.getText()));     
        return model;
    }
        
    public void setModel(NhatKiNhapSach model) {
        txtMaSach.setText(model.getMaSach());
        txtSoLuong.setText(String.valueOf(model.getSoLuong()));
        txtNgayNhap.setText(DateHelper.toString(model.getNgayNhap()));        
    }
    
    void clickM(){
        index =tblNhatKi.getSelectedRow();
        txtMaSach.setText(tblNhatKi.getValueAt(index, 0).toString());//lấy hàng được chọn, lấy dữ liệu hàng hiện tại đưa vào cột thứ 0 set lên textfield
        txtSoLuong.setText(tblNhatKi.getValueAt(index,1).toString());
        txtNgayNhap.setText(tblNhatKi.getValueAt(index,2).toString());        
               
    }

